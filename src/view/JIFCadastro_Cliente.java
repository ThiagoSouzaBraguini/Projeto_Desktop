    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
       try{
            if(validarCampos()){
                if(preencherObjeto()){
                
                    if(DAO.incluir(obj)){
                        JOptionPane.showMessageDialog(this, "SALVO COM SUCESSO", "MEU SISTEMA", JOptionPane.INFORMATION_MESSAGE);
                        jtfCodigo.setText("");
                        jtfNome.setText("");
                        jtfEmail.setText("");
                    }
                    else{
                        JOptionPane.showMessageDialog(this, "NÃO SALVOU", "MEU SISTEMA", JOptionPane.OK_OPTION);
                    }
                }
            }
        }
        catch (Exception erro) {
               JOptionPane.showMessageDialog(this, "Erro no sistema" + erro.getMessage());
        }
    }//GEN-LAST:event_jbSalvarActionPerformed

