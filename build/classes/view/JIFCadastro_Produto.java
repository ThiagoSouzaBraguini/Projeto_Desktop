    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        try{
            if(validarCampos()){
                if(preencherObjeto()){
                
                    if(DAO.incluir(obj)){
                        JOptionPane.showMessageDialog(this, "SALVO COM SUCESSO", "MEU SISTEMA", JOptionPane.INFORMATION_MESSAGE);
                        jtfCodigo.setText("");
                        jtfNome.setText("");
                        jtfPreco.setText("");
                    }
                    else{
                        JOptionPane.showMessageDialog(this, "NÃO SALVOU", "MEU SISTEMA", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
        }
        catch (Exception e) {
               JOptionPane.showMessageDialog(this, "Erro no sistema" + e.getMessage());
        }
    }//GEN-LAST:event_jbSalvarActionPerformed

    private boolean validarCampos(){
        String msg="";
        if(jtfCodigo.getText().length()<3)
        msg+="\nO código deve ter pelo menos 3 números";
        if(jtfCodigo.getText().length()>10)
        msg+="\nO código deve ter menos de 10 números";
        
        if(jtfNome.getText().length()<3)
        msg+="\nO nome deve ter pelo menos 3 caracteres";
        if(jtfNome.getText().length()>25)
        msg+="\nO nome deve ter menos de 25 caracteres";
       
        if(jtfPreco.getText().length()==0)
        msg+="\nO preço não pode ser nulo";
        
        if(msg.length()==0)
            return true; 
        else{
            JOptionPane.showMessageDialog(this,"Corrija os seguintes campos" + msg, "Acesso Negado", JOptionPane.ERROR_MESSAGE);

            return false;
            
        }
    }
    
    private boolean preencherObjeto()throws Exception{
        obj.setCodigo(Integer.parseInt(jtfCodigo.getText()));
        obj.setNome(jtfNome.getText());
        obj.setPreco(Double.parseDouble(jtfPreco.getText()));
        return true;
    }
    
    
